# 06 - TUPLE & SET IN PYTHON

# Tuple = mirip list, tapi bersifat imutable (tidak dapat mengubah value)
# Use ()
# Untuk iteration (pengulangan) akan lebih cepat dari list
tup = (34,65,73,'navin',3.9)
print(tup[1])

# Set = mirip list, tapi acak dan tidak support multiple values (value yang sama tidak akan ditampilkan)
# Use {}
# value outputnya tidak beraturan
set = {23,54,'penk',7.3,'penk'}
print(set)
