# 11 - OPERATORS IN PYTHON

# =========== 1. Arithmetic Operators ===========
x = 69
y = 17
print(x+y)
print(x-y)
print(x*y)
print(x/y)
print(x%y)
print()

# =========== 2. Assignment Operators ===========
x = 3
print(x)
x = x + 2
print(x)
x += 2
print(x)
x *= x
print(x)
a,b = 7,2
print(a)
print(b)
print()

# =========== 3. Unary Operators ===========
print(+10- -2)
print(18 -+ 24)
print(+8/+5)
print(+9+-+10)
print()

# =========== 4. Relational Operators ===========
n = 10
m = 9
print(n>m)
print(n<m)
print(n==m)
print(n<=m)
print(n>=m)
print(n!=m)
print()

# =========== 5. Logical Operators ===========
i = 13
j = 57
# And (kedua kondisi harus true untuk menghasilkan true)
print(i>j and i<j)
print(i<20 and j<60)
# Or (salah satu atau kedua kondisi harus true untuk menghasilkan true)
print(i==j or i<30)
print(i>50 or j>100)
# Not
p = True
print(p)
print(not p)


print()
