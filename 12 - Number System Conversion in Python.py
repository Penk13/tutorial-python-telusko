# 12 - NUMBER SYSTEM CONVERSION IN PYTHON

# There is 4 format :
# 1. Decimal format 	(base 10) 	: 0-9
# 2. Binary Format 		(base 2) 	: 0-1     	(0b)
# 3. Octal Format 		(base 8) 	: 0-7       (0o)
# 4. HexaDecimal Format	(base 16) 	: 0-9, a-f  (0x)

# Convert Decimal to Binary
print(bin(25))
print(bin(21))
print(bin(52))
print(bin(65))
print()

# Convert Decimal to Octal
print(oct(25))
print(oct(21))
print(oct(52))
print(oct(65))
print()

# Convert Decimal to HexaDecimal
print(hex(25))
print(hex(21))
print(hex(52))
print(hex(65))
print()
